# Write the necessary code calculate the volume and surface area
# of a cylinder with a radius of 3.14 and a height of 5.
# Print out the result.

# Define values for radius, height, and 3.14 for pi. 

radius = 3.14
height = 5
pi = 3.14

# Calculate volume formula: pi * radius^2 * height
volume = pi * radius * radius * height

# Calculate surface area forumula: 2 * pi * r2 + 2 * pi * radius * height
surface_area = 2 * pi * radius * radius + 2 * pi * radius * height

# Display results
print(volume)
print(surface_area)