# Write code to display the area and perimeter
# of a rectangle that has a width of 2.4 and a height of 6.4.

# Determine equation for area: width * height
# Determine equation for perimemeter. 2 * (width + height)

rectangle_width = 2.4
rectangle_height = 6.4

