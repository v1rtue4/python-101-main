# Use the modulo operator to confirm which of the values
# shown below are divisible by seven.

num_1 = 42
num_2 = 137
num_3 = 455
num_4 = 1997

# Check divisibility by 7 with modulo
print(num_1 % 7 == 0)  # True – divisible by 7
print(num_2 % 7 == 0)  # False – remainder is 4
print(num_3 % 7 == 0)  # True – divisible by 7
print(num_4 % 7 == 0)  # False – remainder is 2